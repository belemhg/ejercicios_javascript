// Ejemplo de switch

let diaSemana = 3
switch(diaSemana){
    case 1:
    console.log('///////');
    console.log('lunes');
    break;
    case 2:
    console.log('******');
    console.log('Martes'); 
    break;
    case 3:
    console.log('******');   
    console.log('miercoles'); 
    break;
    default:
    console.log ('Dia de la semana invalido');
    break;

}